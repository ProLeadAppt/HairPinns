/**
 * Refresh src/data/reviews.ts from Google Places API.
 *
 * Setup once:
 *   1. Enable "Places API (New)" in Google Cloud Console for your project.
 *   2. Create an API key. Restrict it to Places API + HTTP referrers = your IPs.
 *   3. Find your Place ID at: https://developers.google.com/maps/documentation/places/web-service/place-id
 *   4. Save to .env:
 *        GOOGLE_PLACES_API_KEY="..."
 *        GOOGLE_PLACE_ID="..."
 *
 * Run:
 *   node scripts/refresh-google-reviews.mjs
 *
 * Note: The (New) API returns at most the 5 most relevant reviews per place.
 * That matches what we embed in schema (top 5), so the cadence-efficient play
 * is to re-run this weekly and commit the diff.
 */
import { readFileSync, writeFileSync, existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

// Load .env
if (existsSync(resolve(root, '.env'))) {
  const env = readFileSync(resolve(root, '.env'), 'utf8');
  env.split('\n').forEach((line) => {
    const m = line.match(/^([^#=]+)=(.*)$/);
    if (m && process.env[m[1].trim()] === undefined) {
      process.env[m[1].trim()] = m[2].trim().replace(/^["']|["']$/g, '');
    }
  });
}

const API_KEY = process.env.GOOGLE_PLACES_API_KEY;
const PLACE_ID = process.env.GOOGLE_PLACE_ID;

if (!API_KEY || !PLACE_ID) {
  console.error('[reviews] Set GOOGLE_PLACES_API_KEY and GOOGLE_PLACE_ID in .env');
  process.exit(1);
}

const url = `https://places.googleapis.com/v1/places/${PLACE_ID}?fields=reviews,rating,userRatingCount,displayName`;

const res = await fetch(url, {
  headers: {
    'X-Goog-Api-Key': API_KEY,
    'X-Goog-FieldMask': 'reviews,rating,userRatingCount,displayName',
  },
});

if (!res.ok) {
  console.error(`[reviews] HTTP ${res.status}:`, await res.text());
  process.exit(1);
}

const data = await res.json();
const placeReviews = data.reviews || [];

if (placeReviews.length === 0) {
  console.warn('[reviews] No reviews returned; leaving reviews.ts unchanged.');
  process.exit(0);
}

// Map to our internal Review shape.
const reviews = placeReviews
  .filter((r) => r.rating === 5) // only 5-star for schema rich-result eligibility
  .slice(0, 8)
  .map((r, i) => ({
    id: String(i + 1),
    author: r.authorAttribution?.displayName || 'Google User',
    rating: 5,
    date: (r.publishTime || '').slice(0, 10), // YYYY-MM-DD
    text: (r.originalText?.text || r.text?.text || '').trim(),
    verified: true,
  }));

// Rebuild reviews.ts keeping the interface and adding a generated marker.
const body = `// AUTO-GENERATED by scripts/refresh-google-reviews.mjs on ${new Date().toISOString().slice(0, 10)}
// Do not edit by hand — re-run the script to update.

export interface Review {
  id: string;
  author: string;
  rating: 5;
  date: string;
  text: string;
  verified?: boolean;
  location?: string;
  service?: string;
}

export const googleReviews: Review[] = ${JSON.stringify(reviews, null, 2)};

export const googleAggregateRating = {
  ratingValue: ${data.rating ?? 4.9},
  reviewCount: ${data.userRatingCount ?? 53},
  bestRating: 5,
};
`;

writeFileSync(resolve(root, 'src/data/reviews.ts'), body, 'utf8');
console.log(`[reviews] Wrote ${reviews.length} reviews to src/data/reviews.ts`);
console.log(`[reviews] Aggregate: ${data.rating} (${data.userRatingCount} reviews)`);
